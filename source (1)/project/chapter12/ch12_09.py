import math

print('fabs :', math.fabs(-3.1415))       # 절댓값
print('ceil :', math.ceil(-3.1415))         # 올림
print('floor :', math.floor(-3.1415))      # 내림
print('trunc :', math.trunc(-3.1415))     # 버림
print('gcd :', math.gcd(9, 21))           # 최대공약수
print('factorial :', math.factorial(7))      # 팩토리얼
print('sqrt :', math.sqrt(9))                # 제곱근
