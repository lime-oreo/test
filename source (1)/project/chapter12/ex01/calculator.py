def addition(n1, n2):
    print('덧셈 연산 결과: ', n1 + n2)
	
def subtraction(n1, n2):
    print('뺄셈 연산 결과: ', n1 - n2)
	
def multiplication(n1, n2):
    print('곱셈 연산 결과: ', n1 * n2)
	
def division(n1, n2):
    print('나눗셈 연산 결과: ', n1 / n2)
	
def rest(n1, n2):
    print('나머지 연산 결과: ', n1 % n2)
	
def portion(n1, n2):
    print('몫 연산 결과: ', n1 // n2)
